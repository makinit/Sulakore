namespace Sulakore.Habbo
{
    /// <summary>
    /// Specifies the different gender types in-game.
    /// </summary>
    public enum HGender
    {
        /// <summary>
        /// Represents an unidentified player gender.
        /// </summary>
        Unknown = 0,
        /// <summary>
        /// Represents a male player.
        /// </summary>
        Male = 'M',
        /// <summary>
        /// Represents a female player.
        /// </summary>
        Female = 'F'
    }
}