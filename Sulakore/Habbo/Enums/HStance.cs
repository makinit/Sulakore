namespace Sulakore.Habbo
{
    /// <summary>
    /// Specifies the state of a player.
    /// </summary>
    public enum HStance
    {
        /// <summary>
        /// Represents a player that is standing.
        /// </summary>
        Stand = 0,
        /// <summary>
        /// Represents a player that is sitting.
        /// </summary>
        Sit = 1,
        /// <summary>
        /// Represents a player that is laying down.
        /// </summary>
        Lay = 2
    }
}